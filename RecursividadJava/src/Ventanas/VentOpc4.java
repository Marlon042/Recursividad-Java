    private void btnMostrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarActionPerformed

        int contPar = 0;
        int contImpar = 0;

        int A[] = new int[100]; //Array 100 enteros

        try {
            for (int i = 0; i < 100; i++) {
                A[i] = (int) (Math.random() * 100 - 1);
                this.txtArea1.setText("" + Arrays.toString(A) + "\n"); //Se muestra el array en txtArea1

                if (A[i] % 2 == 0) {
                    contPar = contPar + 1;
                } else {
                    if (A[i] % 2 != 0) {
                        contImpar = contImpar + 1;
                    }
                    this.txtArea2.setText("Numeros Pares: " + contPar + "\n" + "Numeros Impares: " + contImpar + "\n");//Imprime en txtArea2

                }

            }
            this.parImpar(A, 0);

        } catch (Exception e) {
            System.out.println("Error");

        }
    }//GEN-LAST:event_btnMostrarActionPerformed

    public int parImpar(int t[], int pos) {

        try {

            if (pos < t.length) {

                //
                //"El valor " + t[pos] + " es par\n"
                if (t[pos] % 2 == 0) {

                    System.out.println("El valor " + t[pos] + " es par");

                } else {

                    System.out.println("El valor " + t[pos] + " es impar");
                    //this.txtArea2.setText("El valor "+ t[pos]+" es impar\n");

                }

                parImpar(t, pos + 1);
                //this.txtArea2.setText("El par " + t[pos] + " es es mas alto\n");
            }

            //return 1;
        } catch (Exception e) {
            System.out.println("Error");

        }
        return 1;
    }

