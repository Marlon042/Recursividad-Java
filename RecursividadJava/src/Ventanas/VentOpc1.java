    //MÃ©todo Invertir Recursivamente
    public int invertirNumero(int numero) {
        try {
            if (numero < 10) {
                return numero;
            } else {
                int contador = 0;
                int aux = numero;
                while (aux / 10 != 0) {
                    contador++;
                    aux = aux / 10;
                }
                return (int) (Math.pow(10, contador)) * (numero % 10)
                        + this.invertirNumero(numero / 10); //La llamada recursiva

            }
        } catch (InputMismatchException e) {
            System.out.println("Error");

        }
        return 0;
    }

