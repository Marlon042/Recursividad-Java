    public int sumarN(int n) {
        try {
            if (n < 10) {
                return n;
            } else {

                return sumarN(n / 10) + (n % 10);//Parte recursiva
            }

        } catch (InputMismatchException e) {
            System.out.println("Error");
        }
        return 0;
    }
